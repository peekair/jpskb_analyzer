int jpskb_dumpres(char *jpskb_file_path, char *jpskb_res_name)
{
	
	HANDLE							hMainFile		= INVALID_HANDLE_VALUE;
	HANDLE							hSkbFile		= INVALID_HANDLE_VALUE;
	HANDLE							hMapping		= NULL;
	LPVOID							lpMapBase		= NULL;

	PIMAGE_DOS_HEADER				pDosHeader		= NULL;
	PIMAGE_NT_HEADERS				pNtHeader		= NULL;

	PIMAGE_RESOURCE_DIRECTORY		pResTopDir		= NULL;
	PIMAGE_RESOURCE_DIRECTORY		pResTxtDir		= NULL;
	PIMAGE_RESOURCE_DIRECTORY		pResSkbDir		= NULL;

	PIMAGE_RESOURCE_DIRECTORY_ENTRY pResDirEntry	= NULL;
	PIMAGE_RESOURCE_DIRECTORY_ENTRY pResCurEntry	= NULL;
	PIMAGE_RESOURCE_DIRECTORY_ENTRY pResSkbEntry	= NULL;

	PIMAGE_RESOURCE_DATA_ENTRY	pResSkbDataEntry	= NULL;


	LPVOID							pNameData		= NULL;

	LPVOID							lpSkbData		= NULL;
	DWORD							dwSkbSize		= 0;
	DWORD							dwByteWritten	= 0;

	int								nRetCode		= 0;
	int								i				= 0;

	BYTE ucDirName[] = {0x03,0x00,0x54,0x00,0x58,0x00,0x54,0x00};
	BYTE ucSkbName[] = {0x03,0x00,0x53,0x00,0x4B,0x00,0x42,0x00};


	//将主程序读入内存镜像
	hMainFile = CreateFile(jpskb_file_path,GENERIC_READ,FILE_SHARE_READ,NULL,OPEN_EXISTING,FILE_ATTRIBUTE_NORMAL,NULL);
	if (hMainFile == INVALID_HANDLE_VALUE)
	{
		printf("\r\n  [LOG_INFO]:Create %s Failed",jpskb_file_path);
		goto Exit0;
	}

	hMapping = CreateFileMapping(hMainFile,NULL,PAGE_READONLY,0,0,NULL);
	if (hMapping == NULL)
	{
		printf("\r\n  [LOG_INFO]:CreateMapping %s Failed",jpskb_file_path);
		goto Exit0;
	}	

	lpMapBase = MapViewOfFile(hMapping,FILE_MAP_READ,0,0,0);
	if (lpMapBase == NULL)
	{
		printf("\r\n  [LOG_INFO]:MapViewOfFile %s Failed",jpskb_file_path);
		goto Exit0;
	}	

	//定位资源节,由于主程序是在内存中dump,且未经过优化工具优化,故FileOffset == VirtualOffset
	pDosHeader = (PIMAGE_DOS_HEADER)lpMapBase;
	pNtHeader = (PIMAGE_NT_HEADERS)((DWORD)lpMapBase + pDosHeader->e_lfanew);
	pResTopDir = (PIMAGE_RESOURCE_DIRECTORY)((DWORD)lpMapBase + pNtHeader->OptionalHeader.DataDirectory[2].VirtualAddress);

	//在资源节中查找"TXT"
	for (i = 0; i < pResTopDir->NumberOfNamedEntries; i++)
	{
		pResDirEntry = (PIMAGE_RESOURCE_DIRECTORY_ENTRY)((DWORD)pResTopDir + sizeof(IMAGE_RESOURCE_DIRECTORY));
		if ((pResDirEntry->Name >> 31) == 1)
		{
			pNameData = (LPVOID)((DWORD)pResTopDir + (pResDirEntry->Name & 0x0000FFFF));
			if (memcmp(pNameData,ucDirName,8) == 0)
			{
				pResTxtDir = (PIMAGE_RESOURCE_DIRECTORY)((DWORD)pResTopDir + (pResDirEntry->OffsetToData & 0x7FFFFFFF));
				break;
			}
		}
	}

	//在TXT目录中查找名为"SKB"的资源
	if (pResTxtDir != NULL)
	{
		pResCurEntry = (PIMAGE_RESOURCE_DIRECTORY_ENTRY)((DWORD)pResTxtDir + sizeof(IMAGE_RESOURCE_DIRECTORY));
		for(i = 0; i < pResTxtDir->NumberOfNamedEntries; i++)
		{
			pNameData = (LPVOID)((DWORD)pResTopDir + (pResCurEntry->Name & 0x0000FFFF));
			if (memcmp(pNameData,ucSkbName,8) == 0)
			{
				pResSkbDir = (PIMAGE_RESOURCE_DIRECTORY)((DWORD)pResTopDir + (pResCurEntry->OffsetToData & 0x0000FFFF));
				break;
			}
			pResCurEntry = (PIMAGE_RESOURCE_DIRECTORY_ENTRY)((DWORD)pResCurEntry + sizeof(IMAGE_RESOURCE_DIRECTORY_ENTRY));
		}
	}

	//继续读取目录
	if (pResSkbDir != NULL)
	{
		pResSkbEntry = (PIMAGE_RESOURCE_DIRECTORY_ENTRY)((DWORD)pResSkbDir + sizeof(IMAGE_RESOURCE_DIRECTORY));
		for (i = 0; i < pResSkbDir->NumberOfIdEntries; i++)
		{
			pResSkbDataEntry = (PIMAGE_RESOURCE_DATA_ENTRY)((DWORD)pResTopDir + pResSkbEntry->OffsetToData & 0x0FFFFFFF);
			break;
		}
	}

	//读取目录结构中的IMAGE_RESOURCE_DATA_ENTRY,记录资源数据的地址和大小
	if (pResSkbDataEntry != NULL)
	{
		lpSkbData = (LPVOID)((DWORD)lpMapBase + pResSkbDataEntry->OffsetToData);
		dwSkbSize = pResSkbDataEntry->Size;
	}

	//将找到的TXT-SKB文件保存为临时文件,方便以后解密和后继研究
	if ((lpSkbData != NULL) && (dwSkbSize != 0))
	{
		hSkbFile = CreateFile(jpskb_res_name,GENERIC_WRITE,FILE_SHARE_WRITE,NULL,CREATE_ALWAYS,FILE_ATTRIBUTE_NORMAL,NULL);
		if (hSkbFile == INVALID_HANDLE_VALUE)
		{
			printf("\r\n  [LOG_INFO]:CreateFile %s Failed",jpskb_res_name);
			goto Exit0;
		}
		
		SetFilePointer(hSkbFile,0,0,FILE_BEGIN);
		
		WriteFile(hSkbFile,lpSkbData,dwSkbSize,&dwByteWritten,NULL);
		if (dwSkbSize != dwByteWritten)
		{
			printf("\r\n  [LOG_INFO]:WriteFile %s Failed",jpskb_res_name);
			goto Exit0;
		}
	}

Exit1:
	nRetCode = 1;

Exit0:

	//释放资源
	if (lpMapBase != NULL)
	{
		UnmapViewOfFile(lpMapBase);
		lpMapBase = NULL;
	}

	if (hMapping != NULL)
	{
		CloseHandle(hMapping);
		hMapping = NULL;
	}

	if (hMainFile != INVALID_HANDLE_VALUE)
	{
		CloseHandle(hMainFile);
		hMainFile = INVALID_HANDLE_VALUE;
	}

	if (hSkbFile != INVALID_HANDLE_VALUE)
	{
		CloseHandle(hSkbFile);
		hSkbFile = INVALID_HANDLE_VALUE;
	}

	return nRetCode;
}

int	jpskb_decrypt(char *lpszSrcFilePath, char *lpszDstFilePath)
{
	HANDLE hSrcFile				= INVALID_HANDLE_VALUE;
	HANDLE hDstFile				= INVALID_HANDLE_VALUE;

	DWORD  dwFileSize			= 0;	

	LPVOID lpSrcBuffer			= NULL;
	LPVOID lpDstBuffer			= NULL;
	
	DWORD nNumberOfBytesToRead	= 0;
	DWORD nNumberOfBytesRead	= 0;

	DWORD nNumberOfBytesToWrite = 0;
	DWORD nNumberOfBytesWritten = 0;

	DWORD dwCompressedLength	= 0;
	DWORD dwUncompressLength	= 0;

	int   nRetCode				= 0;

	DWORD dwMemSize				= 0;

	DWORD dwDeltaValue			= 0x00000440;

	DWORD dwDecryptLength[4]	= {0};
	
	byte *dst_buffer			= NULL;
	DWORD dwSectionPos			= 0;
	int	  i						= 0;

	//读取dump_bin文件
	hSrcFile = CreateFile(lpszSrcFilePath,GENERIC_READ,FILE_SHARE_READ,NULL,OPEN_EXISTING,FILE_ATTRIBUTE_NORMAL,0);
	if (hSrcFile == INVALID_HANDLE_VALUE)
	{
		printf("\r\n  [LOG_INFO]:Create Src File Failed");
		goto Exit0;
	}

	dwFileSize = GetFileSize(hSrcFile,NULL);
	lpSrcBuffer = VirtualAlloc(NULL,dwFileSize+sizeof(DWORD),MEM_COMMIT,PAGE_READWRITE);
	if (lpSrcBuffer == NULL)
	{
		printf("\r\n  [LOG_INFO]:VirtualAlloc Src Buffer Failed");
		goto Exit0;
	}

	//前四个字节为即将解密出来的数据的大小
	SetFilePointer(hSrcFile,0,0,FILE_BEGIN);
	ReadFile(hSrcFile,&dwMemSize,sizeof(DWORD),&nNumberOfBytesRead,NULL);
	if (nNumberOfBytesRead != sizeof(DWORD))
	{
		printf("\r\n  [LOG_INFO]:ReadFile Failed");
		goto Exit0;
	}

	//申请内存:用于存放解密出来的数据
	lpDstBuffer = VirtualAlloc(NULL,dwMemSize+sizeof(DWORD),MEM_COMMIT,PAGE_READWRITE);
	if (lpDstBuffer == NULL)
	{
		printf("\r\n  [LOG_INFO]:VirtualAlloc Dst Buffer Failed");
		goto Exit0;
	}

	//将指针移动到被加密的数据起始地址
	SetFilePointer(hSrcFile,sizeof(DWORD),0,FILE_BEGIN);
	nNumberOfBytesToRead = dwFileSize - sizeof(DWORD);
	
	//将被压缩的数据读入内存
	ReadFile(hSrcFile,lpSrcBuffer,nNumberOfBytesToRead,&nNumberOfBytesRead,NULL);
	if (nNumberOfBytesRead != nNumberOfBytesToRead)
	{
		printf("\r\n  [LOG_INFO]:Read Source Buffer Failed");
		goto Exit0;
	}

	dwCompressedLength = nNumberOfBytesToRead;
	dwUncompressLength = dwMemSize;

	//解密被压缩的数据
	nRetCode = uncompress((Bytef*)lpDstBuffer,&dwUncompressLength,(Bytef*)lpSrcBuffer,dwCompressedLength);
	switch (nRetCode)
	{
	case Z_OK:
		printf("\r\n  [LOG_INFO] uncompress is ok");
		break;
	default:
		printf("\r\n  [LOG_INFO] uncompress is wrong");
		goto Exit0;
		break;
	}

	//decrypt section-0
	printf("\r\n  [LOG_INFO] decrypt first part");
	dst_buffer = (byte*)lpDstBuffer;
	dwDecryptLength[0] = (dwDeltaValue << 4) - 0xBA;
	for (i = 0; i < dwDecryptLength[0]; i++)
	{
		dst_buffer[dwSectionPos] = key_00[dst_buffer[dwSectionPos]];
		dwSectionPos++;
	}

	//decrypt section-1
	printf("\r\n  [LOG_INFO] decrypt second part");
	dwDecryptLength[1] = (dwDeltaValue * 0x1C + 0x014F) - (dwDecryptLength[0]);
	for (i = 0; i < dwDecryptLength[1]; i++)
	{
		dst_buffer[dwSectionPos] = key_01[dst_buffer[dwSectionPos]];
		dwSectionPos++;
	}

	//decrypt section-2
	printf("\r\n  [LOG_INFO] decrypt third part");
	dwDecryptLength[2] = (dwDeltaValue * 0x02A6 + 0x05B7) - (dwDeltaValue * 0x1C + 0x014F);
	for (i = 0; i < dwDecryptLength[2]; i++)
	{
		dst_buffer[dwSectionPos] = key_02[dst_buffer[dwSectionPos]];
		dwSectionPos++;
	}

	//decrypt section-3
	printf("\r\n  [LOG_INFO] decrypt forth part");
	dwDecryptLength[3] = dwUncompressLength -1 - (dwDeltaValue * 0x02A6 + 0x05B7);
	for (i = 0; i < dwDecryptLength[3]; i++)
	{
		dst_buffer[dwSectionPos] = key_03[dst_buffer[dwSectionPos]];
		dwSectionPos++;
	}

	//导出明文
	printf("\r\n  [LOG_INFO] create skb_info file");
	hDstFile = CreateFile(lpszDstFilePath,GENERIC_WRITE,FILE_SHARE_WRITE,NULL,CREATE_ALWAYS,FILE_ATTRIBUTE_NORMAL,0);
	if (hDstFile == INVALID_HANDLE_VALUE)
	{
		printf("\r\n  [LOG_INFO]:Create Decrypt File Failed");
		goto Exit0;
	}

	SetFilePointer(hDstFile,0,0,FILE_BEGIN);

	nNumberOfBytesToWrite = dwUncompressLength;
	
	printf("\r\n  [LOG_INFO] write data skb_info file");
	WriteFile(hDstFile,dst_buffer,nNumberOfBytesToWrite,&nNumberOfBytesWritten,NULL);
	if (nNumberOfBytesToWrite != nNumberOfBytesWritten)
	{
		printf("\r\n  [LOG_INFO]:Write Failed");
		goto Exit0;
	}
	
Exit1:
	nRetCode = 1;

Exit0:

	//释放资源
	if (lpSrcBuffer != NULL)
	{
		VirtualFree(lpSrcBuffer,dwFileSize+sizeof(DWORD),MEM_DECOMMIT);
		lpSrcBuffer = NULL;
	}

	if (lpDstBuffer != NULL)
	{
		VirtualFree(lpDstBuffer,dwUncompressLength + sizeof(DWORD),MEM_DECOMMIT);
		lpDstBuffer = NULL;
	}

	if (hSrcFile != INVALID_HANDLE_VALUE)
	{
		CloseHandle(hSrcFile);
	}

	if (hDstFile != INVALID_HANDLE_VALUE)
	{
		CloseHandle(hDstFile);
	}

	return nRetCode;
}



